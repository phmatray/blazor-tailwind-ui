namespace DaisyBlazor;

/// <summary>Shape applied by the daisyUI <c>mask-*</c> utility on a <see cref="Mask"/> component.</summary>
public enum MaskShape
{
    /// <summary>Squircle clip (<c>mask-squircle</c>).</summary>
    Squircle,

    /// <summary>Circle clip (<c>mask-circle</c>).</summary>
    Circle,

    /// <summary>Hexagon clip (<c>mask-hexagon</c>).</summary>
    Hexagon,

    /// <summary>Star clip (<c>mask-star</c>).</summary>
    Star,

    /// <summary>Six-point star clip (<c>mask-star-2</c>).</summary>
    Star2,

    /// <summary>Triangle clip (<c>mask-triangle</c>).</summary>
    Triangle,

    /// <summary>Heart clip (<c>mask-heart</c>).</summary>
    Heart,

    /// <summary>Diamond clip (<c>mask-diamond</c>).</summary>
    Diamond,

    /// <summary>Pentagon clip (<c>mask-pentagon</c>).</summary>
    Pentagon
}
