namespace DaisyBlazor;

/// <summary>Alignment modifier for the daisyUI <c>stack</c> utility on a <see cref="StackOverlay"/> component.</summary>
public enum StackAlign
{
    /// <summary>Align children to the top (<c>stack-top</c>).</summary>
    Top,

    /// <summary>Align children to the bottom (<c>stack-bottom</c>).</summary>
    Bottom,

    /// <summary>Align children to the start (left in LTR) (<c>stack-start</c>).</summary>
    Start,

    /// <summary>Align children to the end (right in LTR) (<c>stack-end</c>).</summary>
    End
}
